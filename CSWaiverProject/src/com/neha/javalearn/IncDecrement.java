package com.neha.javalearn;

public class IncDecrement
{
    public static void main(String[] args) {
        //Incrementing a Number
        int trial=4;
        System.out.println( "This is my original number:  "+ trial );
        trial ++;
        System.out.println( "This is my incremented number:  "+ trial );

        //Decrementing a Number
        int trial2=4;
        trial2 --;
        System.out.println( "This is my decremented number:  "+ trial2 );

    }
}
